package java_baekjoon_B.basic_syntax.repeat;
import java.util.*;

public class Q10950_B5 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int A,B,T;
        T = sc.nextInt();
        for(int i=0;i<T;i++) {
            A = sc.nextInt();
            B = sc.nextInt();
            System.out.println(A+B);
        }
    }    
}
